package com.powerdata.openpa.tools;

public class SpSymMatrixBase
{
	protected LinkNet _net;

	protected SpSymMatrixBase(LinkNet net)
	{
		_net = net;
	}
	
	public SpSymMtrxFactPattern savePattern(int[] ref)
	{
		SpSymMtrxFactPattern pat = new SpSymMtrxFactPattern();
		pat.eliminate(_net, ref);
		return pat;
	}
	
	

}
